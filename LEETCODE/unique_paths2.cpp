    int M,N;
    int dfs(vector<vector<int>>& obstacleGrid,int i,int j){
        if(i == M-1 && j == N-1)
            return 1;
        if(i==M || j==N || obstacleGrid[i][j] == 1 )
            return 0;
        return dfs(obstacleGrid,i+1,j) + dfs(obstacleGrid,i,j+1);
    }
    
